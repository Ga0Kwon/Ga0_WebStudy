package 과제.과제9.classExample.Ex1;

public class 해달 extends 포유류{
	
	@Override
	public void eat() {
		
		System.out.println("해달이 밥을 먹습니다.");
	}
	
	@Override
	public void move() {
		System.out.println("해달이 움직입니다.");
	}
	
	@Override
	public void sleep() {
		System.out.println("해달이 잠을 잡니다.");
	}
	
	public void swim() {
		System.out.println("해달은 바다에서 헤엄을 치고 있습니다.");
	}
}
